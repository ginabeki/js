// function is basically a state of statetement that performs a task or calculate a value
function greet() {
  // body of the function, where we add logic of the function
  console.log("Hello world");
}
// call the function
greet();
// functions can have inputs, example here
function greet(name) {
  console.log("Hello " + name);
}
greet("john"); // john is an argument, while name is parameter
// a function can have multiple parameters
